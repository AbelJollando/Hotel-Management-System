    private void checkInButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_checkInButtonMouseClicked
        //pendeklarasian objek
        int id = 1;
        String name = fullNameField.getText();
        String idProof = IDProofField.getText();
        String phoneNumber = phoneNumberField.getText();
        String address = addressField.getText();
        String nationality =(String)Nationality.getSelectedItem();
        String gender = (String)Gender.getSelectedItem();
        String date = dateField.getText();
        String bedType = (String)bedTypeField.getSelectedItem();
        String room = (String)roomTypeField.getSelectedItem();
        String roomNumber = (String)roomNumberField.getSelectedItem();
        String roomPrice = priceField.getText();
        String Query = "select max(id) from customer";
        if(name.isEmpty()||idProof.isEmpty()||phoneNumber.isEmpty()||address.isEmpty()||nationality.isEmpty()||gender.isEmpty()||date.isEmpty()||bedType.isEmpty()||room.isEmpty()||roomNumber.isEmpty()||roomPrice.isEmpty()){
            JOptionPane.showMessageDialog(null, "All Field is Required", "Error", JOptionPane.ERROR_MESSAGE);
        }
        try
        {
            ResultSet rs = Select.getData(Query);
            while(rs.next())
                id=rs.getInt(1);
            id=id+1;
            
            if(!roomPrice.equals(""))
            {
                Query = "update room set status = 'Booked' where roomNumber = '"+roomNumber+"'";
                InsertUpdateDelete.setData(Query, "");
                Query = "insert into customer(id, fullName, phoneNumber, nationality, gender, idproof, address, checkInDate, roomNo, bedType, roomType, pricePerDay) values("+id+", '"+name+"', '"+phoneNumber+"','"+nationality+"', '"+gender+"', '"+idProof+"', '"+address+"', '"+date+"', '"+roomNumber+"', '"+bedType+"', '"+room+"', '"+roomPrice+"')";
                InsertUpdateDelete.setData(Query, "Customer Check In Success");
                setVisible(false);
                new CheckIn().setVisible(true);
                
            }
            
        }
        catch(Exception e)
        {
              JOptionPane.showMessageDialog(null, "Check In Failed");
        } 
    }//GEN-LAST:event_checkInButtonMouseClicked

