    //method untuk menyeleksi kamar sesuai dengan keinginan customer
    public void roomDetails(){
        roomNumberField.removeAllItems();
        priceField.setText("");
        bed = (String)bedTypeField.getSelectedItem();
        roomType = (String)roomTypeField.getSelectedItem();
        roomStatus = "Available";
        
        try
        {
            ResultSet rs = Select.getData("select *from room where bed = '"+bed+"' and roomType='"+roomType+"' and Status = 'Available'");
            while(rs.next())
            {
                roomNumberField.addItem(rs.getString(1));
                priceField.setText(rs.getString(4));
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    //Method untuk Memfilter Kolom hanya Bisa Diisi Huruf
    public void filterHuruf(KeyEvent a){
        if(Character.isDigit(a.getKeyChar())){
            a.consume();
            //Tampilan peringatan jika user memasukkan data berupa angka atau yang lain
            JOptionPane.showMessageDialog(null, "Alphabet or Character Only!", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    //Method Untuk Validasi Inputan User Berupa Angka
    public void filterAngka (KeyEvent b){
        if(Character.isAlphabetic(b.getKeyChar())){
            b.consume();
            //Tampilan peringatan jika user memasukkan karakter yang bukan angka
            JOptionPane.showMessageDialog(null, "Numeric Only!", "Warning!", JOptionPane.WARNING_MESSAGE);       
        }
    }
    
    //method untuk memfilter spasi
    public void filterSpace (KeyEvent c){
        if(Character.isWhitespace(c.getKeyChar())){
            c.consume();
            //Tampilan peringatan jika user menginputkan spasi
            JOptionPane.showMessageDialog(null, "No Spacing", "Warning!", JOptionPane.WARNING_MESSAGE);
        }
    }
    
